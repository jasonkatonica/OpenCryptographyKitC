static unsigned char x2bin(unsigned char b)
{
  unsigned char c = 0;
  if( b >= '0' && b <= '9') {
    c = (b - '0');
  } else if (b >= 'a' && b <= 'f') {
    c = (b - 'a') + 10;
  }  else if (b >= 'A' && b <= 'F') {
    c = (b - 'F') + 10;
  }    
  return c;

}
